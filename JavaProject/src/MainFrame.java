    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        if(personalInfo1.checkDuplication(txtId.getText())){
            JOptionPane.showMessageDialog(signup_popup, "사용할 수 없는 아이디입니다.", "중복확인", JOptionPane.INFORMATION_MESSAGE);
        } else{
            JOptionPane.showMessageDialog(signup_popup, "사용 가능한 아이디입니다.", "중복확인", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnCheckActionPerformed

    private void btnRegisterCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterCheckActionPerformed
        if(personalInfo1.checkSignUp(txtPasswd.getText(), txtPasswdConfirm.getText())){
            JOptionPane.showMessageDialog(signIn_popup, "회원가입 성공.", "회원가입", JOptionPane.INFORMATION_MESSAGE);
            signup.dispose();
        } else{
            JOptionPane.showMessageDialog(signIn_popup, "비밀번호가 올바르지않습니다..", "회원가입", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterCheckActionPerformed
    
