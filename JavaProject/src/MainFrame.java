    // 로그인 버튼을 누르면 데이터베이스에 저장되어 있는 id와 비교하여 판단하고 비밀번호를 가져와 대조
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(personalInfo1.checkIDPW(jIdField1.getText(), jPasswordField1.getText()) == true){
            SubFrame frame = new SubFrame();   // SubFrame 실행
            frame.updateID(personalInfo1.getId());
            frame.setVisible(true);
            frame.setLocation(800, 300);
            dispose();    // 현재 창 종료
        }
        else{
            JOptionPane.showMessageDialog(signIn_popup, "존재하지않는 회원아이디이거나 틀린 비밀번호입니다.", "로그인실패", JOptionPane.INFORMATION_MESSAGE);   // 로그인 실패 창
        }
    }//GEN-LAST:event_btnLoginActionPerformed
                                 

